package sample;

public class Dot {
    private Double y;
    private Double x;

    public Dot(Double x, Double y) {
        this.y = y;
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }
}
